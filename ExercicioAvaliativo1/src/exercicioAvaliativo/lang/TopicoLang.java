package exercicioAvaliativo.lang;

public class TopicoLang {
	public static void main(String[] args) {

		//INTEGER
		
		Integer numero = 5;
		Integer outroNumero = 10;
		
		//compareTo
		numero.compareTo(outroNumero);
		
		//hashCode
		numero.hashCode();
		
		//shortValue
		numero.shortValue();
		
		//floatValue
		numero.floatValue();
		
		//toString
		numero.toString();
		
		//DOUBLE
		
		Double quebrado = 9.5;
		
		//isNaN
		quebrado.isNaN();
		
		//isInfinite
		quebrado.isInfinite();
		
		//byteValue
		quebrado.byteValue();
		
		//toString
		quebrado.toString();
		
		//shortValue
		quebrado.shortValue();
		
		//LONG
		
		Long numero2 = 27L;
		Long num = 43L;
		
		//byteValue
		numero2.byteValue();
		
		//doubleValue
		numero2.doubleValue();
		
		//toString
		numero2.toString();
		
		//compareTo
		numero2.compareTo(num);
		
		//describeConstable
		numero2.describeConstable();
		
		//NUMBER
		
		Number numero3 = 7;
		
		//shortValue
		numero3.shortValue();
		
		//byteValue
		numero3.byteValue();
		
		//doubleValue
		numero3.doubleValue();
		
		//intValue
		numero3.intValue();
		
		//floatValue
		numero3.floatValue();
		
	}

}
